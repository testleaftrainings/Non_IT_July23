package week6.day2;

public class LearnSwich {

	public static void main(String[] args) {
		int day = 4;
		switch(day) {
		case 1:
			System.out.println("Monday");
		break;
		case 2:
			System.out.println("Tuesday");
		break;
		case 3:
			System.out.println("Friday");
		break;
		
		default:
			System.out.println("Invalid selection");
		
		
		}

	}

}
